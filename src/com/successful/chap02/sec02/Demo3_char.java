package com.successful.chap02.sec02;

public class Demo3_char {

	//main 方法的快捷键： main  Alt + / + Enter
	
	public static void main(String[] args) {
		
		/*
		 * 用单引号引起来的单个字符
		 * 
		 * */
		
		char c = 'A';
		System.out.println("c = " +c);
		
		//声明一个字符变量 保存反斜杠
		char c1 = '\\';
		System.out.println("c1 = " + c1);
	}
	
}
