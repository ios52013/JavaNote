package com.successful.chap02.sec03;

public class Demo2 {

	public static void main(String[] args) {
		//声明2个整型变量 分别赋值
		int a = 10;
		int b = 3;

		// + 运算符
		System.out.println(a+ "+" +b+ "=" + (a+b) );
		// - 运算符
		System.out.println(a+ "-" +b+ "=" + (a-b) );
		// * 运算符
		System.out.println(a+ "*" +b+ "=" + (a*b) );
		// / 运算符
		System.out.println(a+ "/" +b+ "=" + (a/b) );
		// % 运算符
		System.out.println(a+ "%" +b+ "=" + (a%b) );



	}

}
